			this.ruleForm.shouji = this.user.shouji
			this.ro.shouji = true;



			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jiankangshangbao`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='xuehao'){
					this.ruleForm.xuehao = obj[o];
					this.ro.xuehao = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='banji'){
					this.ruleForm.banji = obj[o];
					this.ro.banji = true;
					continue;
					}
					if(o=='shouji'){
					this.ruleForm.shouji = obj[o];
					this.ro.shouji = true;
					continue;
					}
					if(o=='tiwen'){
					this.ruleForm.tiwen = obj[o];
					this.ro.tiwen = true;
					continue;
					}
					if(o=='jiankangma'){
					this.ruleForm.jiankangma = obj[o];
					this.ro.jiankangma = true;
					continue;
					}
					if(o=='xingchengma'){
					this.ruleForm.xingchengma = obj[o];
					this.ro.xingchengma = true;
					continue;
					}
					if(o=='shangbaoshijian'){
					this.ruleForm.shangbaoshijian = obj[o];
					this.ro.shangbaoshijian = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
